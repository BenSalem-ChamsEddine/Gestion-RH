package de.cynapsys.pojos;

import java.util.Date;

public class CertificationPojo {

    private String libelleCertif;
    private String lieuCertif;
    private String dateCertif;

    public String getLibelleCertif() {
        return libelleCertif;
    }

    public void setLibelleCertif(String libelleCertif) {
        this.libelleCertif = libelleCertif;
    }

    public String getLieuCertif() {
        return lieuCertif;
    }

    public void setLieuCertif(String lieuCertif) {
        this.lieuCertif = lieuCertif;
    }

    public String getDateCertif() {
        return dateCertif;
    }

    public void setDateCertif(String dateCertif) {
        this.dateCertif = dateCertif;
    }
}
