package de.cynapsys.pojos;

public class FormationPojo {

    private String annee;
    private String libelleForm;
    private String lieu;

    public String getAnnee() {
        return annee;
    }

    public void setAnnee(String annee) {
        this.annee = annee;
    }

    public String getLibelleForm() {
        return libelleForm;
    }

    public void setLibelleForm(String libelleForm) {
        this.libelleForm = libelleForm;
    }

    public String getLieu() {
        return lieu;
    }

    public void setLieu(String lieu) {
        this.lieu = lieu;
    }
}
